package day01;

// 자바 코드의 시작은 main 메서드이다.

public class _01_Start {
    // 유용한 단축키
    // ctrl(+shift) + w : 선택 확장(축소)
    // alt + enter : 자동 임포트 하기
    // shift + F6 : 해당 파일 이름 바꾸기
    // ma + enter : main 클래스 생성
    // ctrl(shift) + F : 찾기(전체문서에서)
    // ctrl(shift) + R : 찾고 바꾸기(전체문서에서)
    // shift(+ctrl) + F10 : 실행하기(현재 커서 있는 파일)
    // shift + enter : 한줄 내리기
    // shift + alr + 방향키 위 or 아래 : 라인 줄 바꿈
    // ctrl + alt + L : 전체 정렬
    // ctrl + alt + i : 한줄 정렬
    // ctrl + d : 복사해서 아래 붙이기
    // ctrl + y :  한 줄 삭제
    // sout + enter : System.out.println();
    // ctrl + space : 자동완성 목록 보여주기


    public static void main(String[] args) {
        System.out.println("김이홍");//문자열
        System.out.println("10" + "10");//문자열+문자열
        System.out.println(10 + 10);//숫자+숫자
        // 문자, 숫자, 논리 등등 ->타입(형)
        // 출력문을 문자로 아무거나 5개 숫자로 아무거나 5개 출력
        System.out.println("dkdk");
        System.out.println("dere");
        System.out.println("dk384");
        System.out.println("----");
        System.out.println("d   ");
        System.out.println(10);
        System.out.println(10.2);
        System.out.println(1000000);
        System.out.println(0.000002);
        System.out.println(102932);
    }
}